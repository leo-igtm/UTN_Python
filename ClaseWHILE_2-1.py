#Dado un número entero n, imprimir todos los números desde n hasta 1 en orden descendente.
numero_entero = input("Ingrese un numero: ")
numero_int = int(numero_entero)

contador = 1

while(contador <= numero_int):
    print(contador)
    contador = contador + 1 # contador += 1 (COMO HACER UN CONTADOR)



